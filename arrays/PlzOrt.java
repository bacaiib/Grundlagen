package arrays;

public class PlzOrt {
    private String plz;
    private String ort;

    public PlzOrt() {
    }

    public PlzOrt(String plz, String ort) {
        this.plz = plz;
        this.ort = ort;
    }

    public String getPlz() {
        return this.plz;
    }

    public void setPlz(String plz) {
        this.plz = plz;
    }

    public String getOrt() {
        return this.ort;
    }

    public void setOrt(String ort) {
        this.ort = ort;
    }
}
